package com.bsecure.vmp.models;

public class RoutesModel {

    private String route_no, route_name, order_allocation_id, order_allocation_date, session;

    public String getRoute_no() {
        return route_no;
    }

    public void setRoute_no(String route_no) {
        this.route_no = route_no;
    }

    public String getRoute_name() {
        return route_name;
    }

    public void setRoute_name(String route_name) {
        this.route_name = route_name;
    }

    public String getOrder_allocation_id() {
        return order_allocation_id;
    }

    public void setOrder_allocation_id(String order_allocation_id) {
        this.order_allocation_id = order_allocation_id;
    }

    public String getOrder_allocation_date() {
        return order_allocation_date;
    }

    public void setOrder_allocation_date(String order_allocation_date) {
        this.order_allocation_date = order_allocation_date;
    }

    public String getSession() {
        return session;
    }

    public void setSession(String session) {
        this.session = session;
    }
}
